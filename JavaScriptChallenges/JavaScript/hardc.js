/*HARD: Create 4 math functions, one called add() that adds 2 numbers, 
one called subtract() that subtracts 2 numbers, one called multiply() 
to multiply 2 numbers, and one called divide() to divide two numbers.*/


function add(x, y) {
    return x + y;
}

var z = add(30, 7);
console.log(z);



function subtract(x, y) {
    return x - y;
}

var z = subtract(30, 7);
console.log(z);



function multiply(x, y) {
    return x * y;
}

var z = multiply(30, 7);
console.log(z);



function divide(x, y) {
    return x / y;
}

var z = divide(30, 7);
console.log(z);